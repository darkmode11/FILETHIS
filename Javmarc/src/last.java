    public void setItem(){
        this.item = cmbProduct.getSelectedItem().toString();
    }
    
    public void setPrice(Double param_price){
        this.price = param_price;
    }
    
    public void definePrice(String param_item){
    
        switch (param_item) {
            case "Coke":
                setPrice(15d);
                break;
            case "Burger":
                setPrice(25d);
                break;
            case "Bread":
                setPrice(30d);
                break;
            case "Juice":
            setPrice(10d);
            break;
            case "None":
                setPrice(0d);
                break;
            default:
                setPrice(0d);
                break;
        }
    }
    
    public void setCash(){
        this.cash = Double.parseDouble(txtCash.getText());
    }
    
    public void setChange(){
        this.change = getCash() - getPrice();
    }
    
    public String getItem(){
        return this.item;
    }
 
    public Double getPrice(){
        return this.price;
    }
   
    public Double getCash(){ 
        return this.cash;
    }
    
    public Double getChange(){
        return this.change;
    }
     
    public void summary(){
            txtItemD.setText(getItem());
            txtPriceD.setText(getPrice().toString());
            txtCashD.setText(getCash().toString());
            txtChangeD.setText(getChange().toString());
    }
     
    
