    private void txtCashKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCashKeyReleased
        // TODO add your handling code here:
        setItem();
        setPrice(getPrice());
        definePrice(getItem());
        setCash();
        setChange();
                
        if("".equals(val1)){
            txtChanger.setText("");
        }
        else{
            
            val1 = Double.parseDouble(txtCash.getText());
            change = val1 - getPrice();
            txtChanger.setText(change.toString());
        }
    }//GEN-LAST:event_txtCashKeyReleased

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        setItem();
        setPrice(getPrice());
        definePrice(getItem());
        setCash();
        setChange();
        
        price = getPrice();
        cash = getCash();
        item = getItem();

        
        if(cash < price || "None".equals(item)){
            SummaryD.setVisible(false);
            Error.setVisible(true);
        }
        else if(cash > price || !"None".equals(item)){
            summary();
            SummaryD.setVisible(true);
            Error.setVisible(false);
            
  
        }
        else{
        
        }
    }//GEN-LAST:event_jButton1ActionPerformed

